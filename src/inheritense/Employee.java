package inheritense;

public class Employee {
	private int id;
	private int num;
	private String name;
	public Employee(int id, int num, String name) {
		super();
		this.id = id;
		this.num = num;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	
	public int getNum() {
		return num;
	}
		public String getName() {
		return name;
	}
	

}
