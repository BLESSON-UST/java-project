package java8;

public class Customers {
	int rid;
	String rname;
	int pno;
	String email;
	String location;
	public Customers(int rid, String rname, int pno, String email, String location) {
		super();
		this.rid = rid;
		this.rname = rname;
		this.pno = pno;
		this.email = email;
		this.location = location;
	}
	public int getRid() {
		return rid;
	}
	
	public String getRname() {
		return rname;
	}
	
	public int getPno() {
		return pno;
	}
	
	public String getEmail() {
		return email;
	}
	
	public String getLocation() {
		return location;
	}
	
	
	
	
	
	

}
