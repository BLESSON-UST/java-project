package java8;

public class Product {
	private int pid;
	String pname;
	int pprize;
	public Product(int pid, String pname, int pprize) {
		super();
		this.pid = pid;
		this.pname = pname;
		this.pprize = pprize;
	}
	public int getPid() {
		return pid;
	}
	
	public String getPname() {
		return pname;
	}
	
	public int getPprize() {
		return pprize;
	}
	
	
	

}
